#include <iostream>

int main(int argc, const char * argv[]) {
    
    for(int i=0; i<10000; i++) {
        std::cout << i << "\n";
        std::cout << "h \n";
        std::cout << "o \n";
        std::cout << "g \n";
        std::cout << "e \n";
    }
    return 0;
}
